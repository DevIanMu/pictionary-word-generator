// Difficulty config
export const DIFFICULTY_CONFIG = {
  easy: { label: "Easy", stars: 1 },
  medium: { label: "Medium", stars: 2 },
  hard: { label: "Hard", stars: 3 }
};

// Word history ring buffer to avoid repeats
const recentWords = [];
const MAX_HISTORY = 20;

/**
 * Get a random word matching the selected categories and difficulty
 * @param {string[]} categories - Selected category keys
 * @param {string} difficulty - Selected difficulty ('easy' | 'medium' | 'hard')
 * @returns {Object} Word object { word, emoji, category, difficulty }
 */
export function getRandomWord(categories, difficulty) {
  let pool = WORDS;

  // Filter by categories
  if (categories && categories.length > 0) {
    pool = pool.filter(w => categories.includes(w.category));
  }

  // Filter by difficulty
  if (difficulty) {
    pool = pool.filter(w => w.difficulty === difficulty);
  }

  // If filtered pool is too small, relax constraints
  if (pool.length < 5) {
    pool = WORDS;
    if (difficulty) {
      pool = pool.filter(w => w.difficulty === difficulty);
    }
  }

  // Exclude recently shown words
  const recentWordSet = new Set(recentWords.map(w => w.word));
  let available = pool.filter(w => !recentWordSet.has(w.word));

  // Fallback if all words are in history
  if (available.length === 0) {
    available = pool;
  }

  const selected = available[Math.floor(Math.random() * available.length)];

  // Add to history ring buffer
  recentWords.push(selected);
  if (recentWords.length > MAX_HISTORY) {
    recentWords.shift();
  }

  return selected;
}

/**
 * Get all available categories
 * @returns {string[]} Array of category keys
 */
export function getAllCategories() {
  return Object.keys(CATEGORY_CONFIG);
}

/**
 * Get word count by category and difficulty
 * @returns {Object} Stats object
 */
export function getWordStats() {
  const stats = {};
  for (const cat of getAllCategories()) {
    stats[cat] = {
      easy: WORDS.filter(w => w.category === cat && w.difficulty === "easy").length,
      medium: WORDS.filter(w => w.category === cat && w.difficulty === "medium").length,
      hard: WORDS.filter(w => w.category === cat && w.difficulty === "hard").length
    };
  }
  return stats;
}
